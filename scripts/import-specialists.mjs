#!/usr/bin/env node

import fs from 'fs';
import path from 'path';

const OWNER = process.env.NEXUS_SPECIALIST_SOURCE_OWNER ?? '';
const REPO = process.env.NEXUS_SPECIALIST_SOURCE_REPO ?? '';
const BRANCH = process.env.NEXUS_SPECIALIST_SOURCE_BRANCH ?? 'main';
const REPO_API = OWNER && REPO
  ? `https://api.github.com/repos/${OWNER}/${REPO}/git/trees/${BRANCH}?recursive=1`
  : '';
const RAW_BASE = OWNER && REPO
  ? `https://raw.githubusercontent.com/${OWNER}/${REPO}/${BRANCH}`
  : '';
const OUTPUT_PATH = path.resolve(process.cwd(), 'src/engines/generated-specialists.ts');

const INCLUDED_DIRS = new Set([
  'design',
  'engineering',
  'game-development',
  'integrations',
  'marketing',
  'paid-media',
  'product',
  'project-management',
  'spatial-computing',
  'specialized',
  'strategy',
  'support',
  'testing',
]);

const SECTION_ALIASES = {
  mission: ['core mission', 'mission', 'role definition', 'primary objective'],
  identity: ['identity & memory', 'identity', 'memory'],
  rules: ['critical rules you must follow', 'critical rules', 'rules'],
  workflow: ['workflow process', 'workflow', 'process'],
  deliverables: ['technical deliverables', 'deliverable template', 'deliverables'],
  communication: ['communication style'],
  learning: ['learning & memory', 'learning'],
  success: ['success metrics'],
};

async function main() {
  const sourceDir = process.argv.includes('--source-dir')
    ? process.argv[process.argv.indexOf('--source-dir') + 1]
    : null;
  const specialists = sourceDir
    ? importFromDirectory(sourceDir)
    : await importFromRemote();

  specialists.sort((left, right) => left.path.localeCompare(right.path));

  const output = [
    '/* eslint-disable */',
    '// Generated by scripts/import-specialists.mjs',
    `// Generated at: ${new Date().toISOString()}`,
    '',
    'export interface ImportedSpecialistSeed {',
    '  id: string;',
    '  name: string;',
    '  description: string;',
    '  division: string;',
    '  path: string;',
    '  emoji?: string;',
    '  color?: string;',
    '  vibe?: string;',
    '  tools: string[];',
    '  sections: Record<string, string>;',
    '  aliases: string[];',
    '  rawMarkdown: string;',
    '}',
    '',
    `export const IMPORTED_SPECIALISTS: ImportedSpecialistSeed[] = ${JSON.stringify(specialists, null, 2)};`,
    '',
  ].join('\n');

  fs.writeFileSync(OUTPUT_PATH, output, 'utf8');
  console.log(`Generated ${specialists.length} specialists → ${path.relative(process.cwd(), OUTPUT_PATH)}`);
}

async function importFromRemote() {
  if (!REPO_API || !RAW_BASE) {
    throw new Error('Set NEXUS_SPECIALIST_SOURCE_OWNER and NEXUS_SPECIALIST_SOURCE_REPO, or provide --source-dir.');
  }
  const tree = await fetchJson(REPO_API);
  const files = (tree.tree ?? [])
    .filter((entry) => entry.type === 'blob' && entry.path.endsWith('.md'))
    .filter((entry) => {
      const top = entry.path.split('/')[0];
      return INCLUDED_DIRS.has(top);
    });

  const specialists = [];
  for (const entry of files) {
    const markdown = await fetchText(`${RAW_BASE}/${entry.path}`);
    specialists.push(parseMarkdown(entry.path, markdown));
  }
  return specialists;
}

function importFromDirectory(sourceDir) {
  const specialists = [];
  for (const filePath of walkMarkdown(sourceDir, true)) {
    const relative = path.relative(sourceDir, filePath).replaceAll(path.sep, '/');
    const markdown = fs.readFileSync(filePath, 'utf8');
    specialists.push(parseMarkdown(relative, markdown));
  }
  return specialists;
}

async function fetchJson(url) {
  const response = await fetch(url, {
    headers: {
      'User-Agent': 'nexus-prime-importer',
      'Accept': 'application/vnd.github+json',
    },
  });
  if (!response.ok) {
    throw new Error(`Failed to fetch ${url}: ${response.status}`);
  }
  return response.json();
}

async function fetchText(url) {
  const response = await fetch(url, {
    headers: {
      'User-Agent': 'nexus-prime-importer',
      'Accept': 'text/plain',
    },
  });
  if (!response.ok) {
    throw new Error(`Failed to fetch ${url}: ${response.status}`);
  }
  return response.text();
}

function parseMarkdown(filePath, rawMarkdown) {
  const { frontmatter, body } = splitFrontmatter(rawMarkdown);
  const division = filePath.split('/')[0];
  const name = String(frontmatter.name ?? inferNameFromPath(filePath));
  const description = String(frontmatter.description ?? '');
  const sections = extractSections(body);

  return {
    id: slugify(filePath.replace(/\.md$/, '')),
    name,
    description,
    division,
    path: filePath,
    emoji: frontmatter.emoji ? String(frontmatter.emoji) : undefined,
    color: frontmatter.color ? String(frontmatter.color) : undefined,
    vibe: frontmatter.vibe ? String(frontmatter.vibe) : undefined,
    tools: normalizeTools(frontmatter.tools),
    sections,
    aliases: buildAliases(name, filePath, division, sections),
    rawMarkdown,
  };
}

function splitFrontmatter(raw) {
  if (!raw.startsWith('---\n')) {
    return { frontmatter: {}, body: raw };
  }
  const end = raw.indexOf('\n---\n', 4);
  if (end === -1) {
    return { frontmatter: {}, body: raw };
  }

  const frontmatterRaw = raw.slice(4, end).trim();
  const body = raw.slice(end + 5).trim();
  return {
    frontmatter: parseLooseFrontmatter(frontmatterRaw),
    body,
  };
}

function parseLooseFrontmatter(raw) {
  const result = {};
  for (const line of raw.split(/\r?\n/)) {
    const idx = line.indexOf(':');
    if (idx === -1) continue;
    const key = line.slice(0, idx).trim();
    const value = line.slice(idx + 1).trim();
    if (!key) continue;
    if (value.startsWith('[') && value.endsWith(']')) {
      result[key] = value.slice(1, -1).split(',').map((entry) => entry.trim()).filter(Boolean);
      continue;
    }
    result[key] = value.replace(/^['"]|['"]$/g, '');
  }
  return result;
}

function extractSections(body) {
  const sections = {};
  let current = 'overview';
  let buffer = [];

  for (const line of body.split(/\r?\n/)) {
    const heading = line.match(/^#{2,3}\s+(.+?)\s*$/);
    if (heading) {
      flushSection(sections, current, buffer);
      current = normalizeHeading(heading[1]);
      buffer = [];
      continue;
    }
    buffer.push(line);
  }

  flushSection(sections, current, buffer);

  for (const [canonical, aliases] of Object.entries(SECTION_ALIASES)) {
    if (sections[canonical]) continue;
    const match = aliases.find((alias) => sections[alias]);
    if (match) sections[canonical] = sections[match];
  }

  return sections;
}

function flushSection(target, key, lines) {
  const text = lines.join('\n').trim();
  if (text) target[key] = text;
}

function normalizeHeading(value) {
  return value
    .toLowerCase()
    .replace(/[`*]/g, '')
    .replace(/[^\w\s&/-]+/g, '')
    .replace(/\s+/g, ' ')
    .trim();
}

function normalizeTools(value) {
  if (Array.isArray(value)) return value.map(String);
  if (typeof value === 'string') {
    return value.split(',').map((entry) => entry.trim()).filter(Boolean);
  }
  return [];
}

function buildAliases(name, filePath, division, sections) {
  const aliases = new Set([
    name,
    division,
    path.basename(filePath, '.md').replace(/[-_]/g, ' '),
  ]);
  const mission = sections.mission || sections['core mission'] || sections['role definition'];
  if (mission) {
    mission.split(/\n+/).slice(0, 4).forEach((line) => {
      if (line.trim()) aliases.add(line.trim());
    });
  }
  return [...aliases].filter(Boolean);
}

function inferNameFromPath(filePath) {
  return path.basename(filePath, '.md')
    .split(/[-_]/)
    .map((chunk) => chunk.charAt(0).toUpperCase() + chunk.slice(1))
    .join(' ');
}

function slugify(value) {
  return value.toLowerCase().replace(/[^a-z0-9]+/g, '-').replace(/^-|-$/g, '');
}

function walkMarkdown(rootDir, topLevel = false) {
  const results = [];
  for (const entry of fs.readdirSync(rootDir, { withFileTypes: true })) {
    const fullPath = path.join(rootDir, entry.name);
    if (entry.isDirectory()) {
      if (topLevel && !INCLUDED_DIRS.has(entry.name)) {
        continue;
      }
      results.push(...walkMarkdown(fullPath, false));
      continue;
    }
    if (entry.isFile() && entry.name.endsWith('.md') && !topLevel) results.push(fullPath);
  }
  return results;
}

main().catch((error) => {
  console.error(error);
  process.exitCode = 1;
});
